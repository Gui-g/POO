package dados;

public class Animal {

	private String nome;
	private String continenteEncontrado;
	private String comidaFavorita;
	private int estimativaVida;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getContinenteEncontrado() {
		return continenteEncontrado;
	}
	public void setContinenteEncontrado(String continenteEncontrado) {
		this.continenteEncontrado = continenteEncontrado;
	}
	public String getComidaFavorita() {
		return comidaFavorita;
	}
	public void setComidaFavorita(String comidaFavorita) {
		this.comidaFavorita = comidaFavorita;
	}
	public int getEstimativaVida() {
		return estimativaVida;
	}
	public void setEstimativaVida(int estimativaVida) {
		this.estimativaVida = estimativaVida;
	}
	
}
